class Flight {
    int id; 
	String name; 
	String source; 
	String dest; 
	double price;
    int seats; 
	String time; 
	String airline; 
	String status; 
	boolean available;

    int getId(int id){ 
	return id; 
	}
    String getName(String name){ 
	return name; 
	}
    String getSource(String s){ 
	return s; 
	}
    String getDest(String d){ 
	return d; 
	}
    double getPrice(double p){ 
	return p; 
	}
    int getSeats(int s){ 
	return s; 
	}
    String getTime(String t){ 
	return t; 
	}
    String getAirline(String a){ 
	return a; 
	}
    String getStatus(String s){ 
	return s; 
	}
    boolean isAvailable(boolean a){ 
	return a; 
	}
}