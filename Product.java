class Product {
    int id; 
	String name; 
	double price; 
	String brand; 
	String category;
    int stock; 
	String color; 
	double weight; 
	String warranty; 
	boolean available;

    int getId(int id){ 
	return id; 
	}
    String getName(String name){ 
	return name; 
	}
    double getPrice(double price){ 
	return price; 
	}
    String getBrand(String brand){ 
	return brand; 
	}
    String getCategory(String category){ 
	return category; 
	}
    int getStock(int stock){ 
	return stock; 
	}
    String getColor(String color){ 
	return color; 
	}
    double getWeight(double weight){ 
	return weight; 
	}
    String getWarranty(String warranty){ 
	return warranty; 
	}
    boolean isAvailable(boolean a){ 
	return a; 
	}
}