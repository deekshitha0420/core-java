class Customer {
    int id; 
	String name; 
	String city; 
	long phone; 
	String email;
    int age; 
	String gender; 
	String type; 
	double balance; 
	boolean active;

    int getId(int id){ 
	return id; 
	}
    String getName(String name){ 
	return name; 
	}
    String getCity(String city){ 
	return city; 
	}
    long getPhone(long phone){ 
	return phone; 
	}
    String getEmail(String e){ 
	return e; 
	}
    int getAge(int age){ 
	return age; 
	}
    String getGender(String g){ 
	return g; 
	}
    String getType(String t){ 
	return t; 
	}
    double getBalance(double b){ 
	return b; 
	}
    boolean isActive(boolean a){ 
	return a; 
	}
}