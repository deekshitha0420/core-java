class Order {
    int id; 
	String product; 
	int quantity; 
	double price; 
	String date;
    String status; 
	String payment; 
	String address; 
	long phone; 
	boolean delivered;

    int getId(int id){ 
	return id; 
	}
    String getProduct(String p){ 
	return p; 
	}
    int getQuantity(int q){ 
	return q; 
	}
    double getPrice(double p){ 
	return p; 
	}
    String getDate(String d){ 
	return d; 
	}
    String getStatus(String s){ 
	return s; 
	}
    String getPayment(String p){ 
	return p; 
	}
    String getAddress(String a){ 
	return a; 
	}
    long getPhone(long p){ 
	return p; 
	}
    boolean isDelivered(boolean d){ 
	return d; 
	}
}