class Student {

    // 10 Instance Variables (States)
    int id;
    String name;
    int age;
    String course;
    double marks;
    String grade;
    String college;
    String city;
    long phone;
    boolean isActive;

    // 10 Methods (Behaviors)

    int getId(int id) {
        return id;
    }

    String getName(String name) {
        return name;
    }

    int getAge(int age) {
        return age;
    }

    String getCourse(String course) {
        return course;
    }

    double getMarks(double marks) {
        return marks;
    }

    String getGrade(String grade) {
        return grade;
    }

    String getCollege(String college) {
        return college;
    }

    String getCity(String city) {
        return city;
    }

    long getPhone(long phone) {
        return phone;
    }

    boolean isActive(boolean status) {
        return status;
    }
}