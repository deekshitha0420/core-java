	String city; 
	String state; 
	int colleges;
    int students; 
	int staff; 
	String type; 
	double rating; 
	boolean approved;

    int getId(int id){ 
	return id; 
	}
    String getName(String name){ 
	return name; 
	}
    String getCity(String city){ 
	return city; 
	}
    String getState(String state){ 
	return state; 
	}
    int getColleges(int c){ 
	return c; 
	}
    int getStudents(int s){ 
	return s; 
	}
    int getStaff(int s){ 
	return s; 
	}
