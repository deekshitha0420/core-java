class Book {
    int id; 
	String title; 
	String author; 
	double price; 
	int pages;
    String publisher; 
	String language; 
	String genre; 
	int year; 
	boolean available;

    int getId(int id){ 
	return id; 
	}
    String getTitle(String title){ 
	return title; 
	}
    String getAuthor(String author){ 
	return author; 
	}
    double getPrice(double price){ 
	return price; 
	}
    int getPages(int pages){ 
	return pages; 
	}
    String getPublisher(String pub){ 
	return pub; 
	}
    String getLanguage(String lang){ 
	return lang; 
	}
    String getGenre(String genre){ 
	return genre; 
	}
    int getYear(int year){ 
	return year; 
	}
    boolean isAvailable(boolean a){ 
	return a; 
	}
}