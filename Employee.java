class Employee {

    int empId;
    String empName;
    int age;
    String department;
    double salary;
    String designation;
    String company;
    String city;
    long phone;
    boolean isWorking;

    int getEmpId(int empId) {
        return empId;
    }

    String getEmpName(String empName) {
        return empName;
    }

    int getAge(int age) {
        return age;
    }

    String getDepartment(String dept) {
        return dept;
    }

    double getSalary(double salary) {
        return salary;
    }

    String getDesignation(String des) {
        return des;
    }

    String getCompany(String company) {
        return company;
    }

    String getCity(String city) {
        return city;
    }

    long getPhone(long phone) {
        return phone;
    }

    boolean isWorking(boolean status) {
        return status;
    }
}