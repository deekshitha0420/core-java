class Library {
    int id; 
	String name; 
	String city; 
	int books; 
	int members;
    int staff; 
	String type; 
	String timing; 
	long phone; 
	boolean open;

    int getId(int id){ 
	return id; 
	}
    String getName(String name){ 
	return name; 
	}
    String getCity(String city){ 
	return city; 
	}
    int getBooks(int b){ 
	return b; 
	}
    int getMembers(int m){ 
	return m; 
	}
    int getStaff(int s){ 
	return s; 
	}
    String getType(String t){ 
	return t; 
	}
    String getTiming(String t){ 
	return t; 
	}
    long getPhone(long p){ 
	return p; 
	}
    boolean isOpen(boolean o){ 
	return o; 
	}
}