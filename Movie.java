class Movie {
    int id; 
	String title; 
	String hero; 
	String director; 
	double rating;
    String language; 
	String genre; 
	int duration; 
	int year; 
	boolean hit;

    int getId(int id){ 
	return id; 
	}
    String getTitle(String title){ 
	return title; 
	}
    String getHero(String hero){ 
	return hero; 
	}
    String getDirector(String d){ 
	return d; 
	}
    double getRating(double r){ 
	return r; 
	}
    String getLanguage(String l){ 
	return l; 
	}
    String getGenre(String g){ 
	return g; 
	}
    int getDuration(int d){ 
	return d; 
	}
    int getYear(int y){ 
	return y; 
	}
    boolean isHit(boolean h){ 
	return h; 
	}
}